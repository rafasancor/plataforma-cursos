    <h1>Sobre Nós</h1>
    <p>A Plataforma de Cursos Online é dedicada a fornecer educação de alta qualidade em diversas áreas do conhecimento,
        com foco em tecnologia, negócios, desenvolvimento pessoal, design e muito mais.</p>
    <p>Nossa missão é capacitar indivíduos através de cursos flexíveis e acessíveis, ministrados por instrutores
        experientes que combinam teoria e prática para oferecer um aprendizado eficiente e aplicável.</p>

    <h2>Nossa História</h2>
    <p>Fundada em 2025, nossa plataforma nasceu da paixão por compartilhar conhecimento e do desejo de tornar a educação
        acessível a todos, independentemente da localização ou da rotina. Começamos com apenas alguns cursos voltados à
        área de TI, e rapidamente expandimos para abranger diversas categorias, atendendo milhares de alunos em todo o
        Brasil.</p>

